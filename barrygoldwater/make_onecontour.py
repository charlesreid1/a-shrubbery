import simplejson as json

z = json.load(open('Elev_Contour.json','r'))

# downselect features to particular contour lines
use_contours = range(250,3050,100)

new_z = {}
new_z['type'] = z['type']
use_features = [z['features'][j] for j in range(len(z['features'])) if z['features'][j]['properties']['CONTOURELE'] in use_contours]
# len 535
new_z['features'] = use_features

with open('barrygoldwater_red.json','w') as f:
    f.write(json.dumps(new_z))






